#Integer - Lesson One 
#Using the built in functions of float and Integer

PI = 22/7 #Use of a constant
radius = float(input("RADIUS - "))
area = abs(PI*radius*radius) #Area cannot be negative
print(f"THE AREA OF THE CIRCLE OF RADIUS {radius} is {area}\n") #Formatted strings
print(f"THE ROUNDED OFF AREA OF THE CIRCLE OF RADIUS {radius} is {round(area)}\n") #Formatted strings and Escape sequence
 


